public class one {
    public static void main(String[] args){
        System.out.println("1 to 10");
        for(int i=1;i<10;i++){
            System.out.println(i);
        }
        System.out.println("10 to 1");
        int i=10;
        while(i>=1){
            System.out.println(i);
            i--;
        }
    }
}